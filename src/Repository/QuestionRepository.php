<?php

namespace App\Repository;

use App\Entity\Question;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Question>
 *
 * @method Question|null find($id, $lockMode = null, $lockVersion = null)
 * @method Question|null findOneBy(array $criteria, array $orderBy = null)
 * @method Question[]    findAll()
 * @method Question[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class QuestionRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Question::class);
    }

    public function add(Question $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Question $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function findAllJoinInternaute()
    {
        return $this->createQueryBuilder('q')
            ->join('q.internaute','i')
            ->select('i.id as internauteId,i.nom,i.email,q.question,q.vu')
            ->orderBy('q.id','desc')
            ->getQuery()
            ->getResult();
    }

    public function findByInternaute($id)
    {
        return $this->createQueryBuilder('q')
            ->join('q.internaute','i')
            ->where('i.id= :id')
            ->setParameter('id',$id)
            ->select('i.nom,i.email,q.question,q.vu')
            ->getQuery()
            ->getResult();
    }
